package com.hms.pharmacy.inventory.model;

public class MedicineGeneric 
{
	public String genericID;
	public String genericName;
	public String genericShortName;

	public String getGenericID() {
		return genericID;
	}
	public void setGenericID(String genericID) {
		this.genericID = genericID;
	}
	public String getGenericName() {
		return genericName;
	}
	public void setGenericName(String genericName) {
		this.genericName = genericName;
	}
	public String getGenericShortName() {
		return genericShortName;
	}
	public void setGenericShortName(String genericShortName) {
		this.genericShortName = genericShortName;
	}


}
